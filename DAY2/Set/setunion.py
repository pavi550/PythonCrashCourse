day1 = {"One","Two","Three","Four"}
day2 = {"Five","Six","Seven"}

print("Day1 set: ",day1)
print("Day2 set: ",day2)
print("------------------"*4)

# Union of the both the sets
print("Union of Day1 and Day2")
print("day1|day2 :\n ",day1|day2)
print("------------------"*4)
print("day1.union(day2) :\n ",day1.union(day2))
print("------------------"*4)
